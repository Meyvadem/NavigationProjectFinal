void drawCharacter(Point character) {
    std::cout << "\x1b[" << character.getY() + 1 << ";" << character.getX() + 1 << "H"; // x ve y koordinatlarına git
    std::cout << "*";
    std::cout.flush();
}

void clearCharacter(Point character) {
    std::cout << "\x1b[" << character.getY() + 1 << ";" << character.getX() + 1 << "H"; // x ve y koordinatlarına git
    std::cout << " ";
    std::cout.flush();
}




void runAStarExample(Location currentLoc, Location destination) {
    Node* start = createNode(currentLoc.getPointPath());
    Node* dest = createNode(destination.getPointPath());

    Node* path = aStarSearch(grid, start, dest);

    printColoredPath(path, grid);
    printMap();

    Point startPoint = {1, 1};
    Point endPoint = {83, 30};
    Point character = currentLoc.getPointDoor();
    char key;

    // Başlangıç pozisyonunda karakteri çiz
    drawCharacter(character);

    do {
        key = getch(); // Kullanıcının tuş girişini bekler

        // Önceki pozisyonda karakteri sil
        clearCharacter(character);

        // Karakterin yeni pozisyonunu belirle
        switch (key) {
            case 'w':
                if (character.getY() > startPoint.getY()) {
                    character.getY()--;
                }
                break;
            case 's':
                if (character.getY() < endPoint.getY()) {
                    character.getY()++;
                }
                break;
            case 'a':
                if (character.getX() > startPoint.getX()) {
                    character.getX()--;
                }
                break;
            case 'd':
                if (character.getX() < endPoint.getX()) {
                    character.getX()++;
                }
                break;
        }

        printColoredPath(path, grid);
        printMap();

        // Yeni pozisyonda karakteri çiz
        drawCharacter(character);

        if (character.getX() == destination.getPointDoor().getX() && character.getY() == destination.getPointDoor().getY()) {
            gotoxy(100, 10);
            std::cout << "You reached " << destination.getName() << "!" << std::endl;
        }

    } while (key != 'q'); // 'q' tuşuna basılana kadar devam et

    delete start;
    delete dest;
}
