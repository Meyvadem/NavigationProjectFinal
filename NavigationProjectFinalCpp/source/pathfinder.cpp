Node* createNode(Point point) {
    Node* node = new Node(point, 0, 0, 0, nullptr);
    return node;
}

int isValid(int x, int y) {
    return (x >= 0 && x < ROWS && y >= 0 && y < COLS);
}

int isDestination(int x, int y, Node* dest) {
    return (x == dest->getPoint().getX() && y == dest->getPoint().getY());
}

int calculateHValue(int x, int y, Node *dest) {
    return MY_ABS(x - dest->getPoint().getX()) + MY_ABS(y - dest->getPoint().getY());
}


Node* aStarSearch(int grid[ROWS][COLS],Node* start, Node* dest){
    if (!isValid(start->getPoint().getX(), start->getPoint().getY()) || !isValid(dest->getPoint().getX(), dest->getPoint().getY())) {
        std::cout << "Geçersiz başlangıç veya hedef konumu." << std::endl;
        return nullptr;
    }

    if (grid[start->getPoint().getX()][start->getPoint().getY()] == 0 || grid[dest->getPoint().getX()][dest->getPoint().getY()] == 0) {
        std::cout << "Başlangıç veya hedef engellidir." << std::endl;
        return nullptr;
    }

    int dx[] = {-1, 0, 1, 0}; // Komşu hücrelerin x koordinatları
    int dy[] = {0, 1, 0, -1}; // Komşu hücrelerin y koordinatları

    // Açık ve kapalı listeler
    std::vector<Node*> openList;
    std::vector<Node*> closedList;

    // Başlangıç düğümünü açık listeye ekle
    openList.push_back(start);

    while (!openList.empty()) {
        // Açık listeyi düğüm maliyetine göre küçükten büyüğe sırala
        std::sort(openList.begin(), openList.end(), [](Node* a, Node* b) {
            return a->getF() < b->getF();
        });

        // Açık listeden en düşük maliyetli düğümü al
        Node* current = openList[0];
        openList.erase(openList.begin());

        // Düğümü kapalı listeye ekle
        closedList.push_back(current);

        // Hedefe ulaşıldı mı kontrol et
        if (isDestination(current->getPoint().getX(), current->getPoint().getY(), dest)) {
            return current; // Hedefe ulaşıldıysa, son düğümü döndür
        }

        // Komşu düğümleri kontrol et
        for (int i = 0; i < 4; i++) {
            int newX = current->getPoint().getX() + dx[i];
            int newY = current->getPoint().getY() + dy[i];
            Point p = {newX, newY};

            // Yeni konum geçerli mi kontrol et
            if (isValid(newX, newY) && grid[newX][newY] == 1) {
                // Yeni düğümü oluştur
                Node* neighbor = createNode(p);

                // Komşu düğüm kapalı listeye zaten ekli mi kontrol et
                auto itClosed = std::find_if(closedList.begin(), closedList.end(), [neighbor](Node* n) {
                    return n->getPoint().getX() == neighbor->getPoint().getX() && n->getPoint().getY() == neighbor->getPoint().getY();
                });

                if (itClosed != closedList.end()) {
                    delete neighbor;
                    continue;
                }

                // G ve H değerlerini hesapla
                neighbor->setG(current->getG() + 1);
                neighbor->setH(calculateHValue(newX, newY, dest));
                neighbor->setF(neighbor->getG() + neighbor->getH());
                neighbor->setParent(current);

                // Komşu düğüm açık listeye zaten ekli mi kontrol et
                auto itOpen = std::find_if(openList.begin(), openList.end(), [neighbor](Node* n) {
                    return n->getPoint().getX() == neighbor->getPoint().getX() && n->getPoint().getY() == neighbor->getPoint().getY();
                });

                // Eğer açık listeye ekli değilse, açık listeye ekle
                if (itOpen == openList.end()) {
                    openList.push_back(neighbor);
                }
                // Eğer açık listeye ekliyse ve yeni g değeri daha küçükse, güncelle
                else {
                    if (neighbor->getG() < (*itOpen)->getG()) {
                        (*itOpen)->setG(neighbor->getG());
                        (*itOpen)->setF(neighbor->getF());
                        (*itOpen)->setParent(neighbor->getParent());
                    }
                }
            }
        }
    }

    // Hedefe ulaşılamadıysa, nullptr döndür
    return nullptr;
}

